use crate::core::{
    bitboard::BitBoard, orientation::Orientation, pieces::PieceTypeId, square::Square,
};

#[derive(Debug, Copy, Clone, PartialEq, Eq)]
pub struct LUTEntry(pub usize);

type Entry = (u8, u8, u64, u64);

impl LUTEntry {
    #[inline]
    pub fn orientation(&self) -> Orientation {
        Orientation::from_index(self.at().0)
    }

    #[inline]
    pub fn position(&self) -> Square {
        Square::new(self.at().1)
    }

    #[inline]
    pub fn mask(&self) -> BitBoard {
        BitBoard(self.at().2)
    }

    #[inline]
    pub fn gaze(&self) -> BitBoard {
        BitBoard(self.at().3)
    }

    #[inline]
    fn at(&self) -> &Entry {
        &MOVE_TAB[self.0]
    }

    #[inline]
    pub fn lookup(
        piece: PieceTypeId,
        position: Square,
        orientation: Orientation,
    ) -> Option<LUTEntry> {
        for (i, m) in MOVE_TAB
            .iter()
            .enumerate()
            .skip(piece.def().lut_offset)
            .take(piece.def().moves)
        {
            if Orientation::from_index(m.0) == orientation && Square::new(m.1) == position {
                return Some(LUTEntry(i));
            }
        }
        None
    }
}

